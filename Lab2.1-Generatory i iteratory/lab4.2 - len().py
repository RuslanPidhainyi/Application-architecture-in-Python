#len - Це вбудована функція, яка повертає кількість елементів у колекції або розмір об’єкта.
#len - Наприклад, для рядків, списків, кортежів чи словників len повертає кількість символів або елементів.

my_list = [1,2,3,4]
print(len(my_list))  # Виведе: 4

my_string = "Hello"
